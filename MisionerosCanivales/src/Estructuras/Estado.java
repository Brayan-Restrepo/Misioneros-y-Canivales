package Estructuras;

/**
 *
 * @author Brayan
 */
public class Estado {
    
    private int misionero;
    private int canival;
    private int lancha;

    public Estado() {
    }

    public int getMisionero() {
        return misionero;
    }

    public void setMisionero(int misionero) {
        this.misionero = misionero;
    }

    public int getCanival() {
        return canival;
    }

    public void setCanival(int canival) {
        this.canival = canival;
    }

    public int getLancha() {
        return lancha;
    }

    public void setLancha(int lancha) {
        this.lancha = lancha;
    }
    
}
